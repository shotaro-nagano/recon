/** 「おすすめ」5枚: 局面が偏らないよう各フェーズから1枚ずつ */
export const RECOMMENDED_TACTIC_IDS = ['t01', 't05', 't11', 't16', 't24'] as const;

export function tacticsByCategory(cat: TacticCategory): TacticCard[] {
  return TACTIC_CARDS.filter((t) => t.cat === cat);
}

export function recommendedTactics(): TacticCard[] {
  return RECOMMENDED_TACTIC_IDS
    .map((id) => TACTIC_CARDS.find((t) => t.id === id))
    .filter((t): t is TacticCard => Boolean(t));
}
